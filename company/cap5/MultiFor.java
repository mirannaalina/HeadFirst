package com.company.cap5;

public class MultiFor {
    public static void main (String[]args){

            for (int x = 0; x < 4; x++) {//0,1,2,3


                for (int y = 4; y > 2; y--) {//odata 4,odata 3

                    System.out.println(x + " " + y);
                }

                if (x == 1) {
                    x++;

                }






            }
    }
}
